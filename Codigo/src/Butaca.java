
public class Butaca {
	
	int x=0, y=0, estado=0, f=0, c=0;

	public Butaca(int x, int y, int estado, int f, int c) {
		super();
		this.x = x;
		this.y = y;
		this.estado = estado;
		this.f = f;
		this.c = c;
	}



	


	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @param x the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * @param y the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}



	/**
	 * @return the estado
	 */
	public int getEstado() {
		return estado;
	}



	/**
	 * @param estado the estado to set
	 */
	public void setEstado(int estado) {
		this.estado = estado;
	}






	/**
	 * @return the f
	 */
	public int getF() {
		return f;
	}






	/**
	 * @param f the f to set
	 */
	public void setF(int f) {
		this.f = f;
	}






	/**
	 * @return the c
	 */
	public int getC() {
		return c;
	}






	/**
	 * @param c the c to set
	 */
	public void setC(int c) {
		this.c = c;
	}

}
